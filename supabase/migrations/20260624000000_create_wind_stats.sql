-- ============================================================
-- wind_stats: country-level kite-wind probabilities (per-country)
-- ============================================================
-- Monthly "% windy days" (>=3h in a row of >=12 kn at a good local spot),
-- scraped from bstoked.net. `months` is 12 values, January..December (1..12),
-- each 0-100. Mirrors web/lib/wind-stats.ts (regenerate together). Common
-- name variants are included as alias rows (is_alias = true) so a direct
-- case-insensitive lookup on a provider/offer country works out of the box.
--
-- Generated from web/lib/wind-stats.ts by scripts; do not hand-edit values.
-- Safe to re-run: CREATE ... IF NOT EXISTS + upsert on conflict.
-- ============================================================

CREATE TABLE IF NOT EXISTS wind_stats (
  country     TEXT PRIMARY KEY,
  months      SMALLINT[] NOT NULL,   -- 12 monthly values, Jan..Dec, 0-100 % windy days
  is_alias    BOOLEAN NOT NULL DEFAULT FALSE,
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_wind_stats_country_lower ON wind_stats (lower(country));

